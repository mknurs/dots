#!/bin/sh
# A simple and primitive fetch script with hardcoded ANSI art.

bold="\e[1m"
# f00="\e[30m"
# f01="\e[31m"
# f02="\e[32m"
# f03="\e[33m"
# f04="\e[34m"
# f05="\e[35m"
# f06="\e[36m"
# f07="\e[37m"
# f08="\e[90m"
# f09="\e[91m"
# f10="\e[92m"
# f11="\e[93m"
f12="\e[94m"
# f13="\e[95m"
# f14="\e[96m"
# f15="\e[97m"
b00="\e[40m"
b01="\e[41m"
b02="\e[42m"
b03="\e[43m"
b04="\e[44m"
b05="\e[45m"
b06="\e[46m"
b07="\e[47m"
b08="\e[100m"
b09="\e[101m"
b10="\e[102m"
b11="\e[103m"
b12="\e[104m"
b13="\e[105m"
b14="\e[106m"
b15="\e[107m"
esc="\e[0m"
colors1="$b00  $b01  $b02  $b03  $b04  $b05  $b06  $b07  $esc"
colors2="$b08  $b09  $b10  $b11  $b12  $b13  $b14  $b15  $esc"

usrhst="$USER@$HOSTNAME"
device="$(cat /sys/devices/virtual/dmi/id/product_family)"
kernel="$(uname -r)"
memory="$(free -h | grep "^Mem" | tr -s ' ' | cut -d ' ' -f3)/$(free -h | grep "^Mem" | tr -s " " | cut -d " " -f2)"
pacman="$(pacman -Q | wc -l) ($(pacman -Qe | wc -l))"

clear
printf "${bold}${f12}"'       /\\\        '"${usrhst}${esc}\n"
printf "${bold}${f12}"'      /  \\\       '"${esc}${bold}device:${esc} ${device}\n"
printf "${bold}${f12}"'     /    \\\      '"${esc}${bold}kernel:${esc} ${kernel}\n"
printf "${bold}${f12}"'    /      \\\     '"${esc}${bold}memory:${esc} ${memory}\n"
printf "${bold}${f12}"'   /   __   \\\    '"${esc}${bold}pacman:${esc} ${pacman}\n"
printf "${bold}${f12}"'  /  _(  )_  \\\   '"${esc}${colors1}\n"
printf "${bold}${f12}"' /_--      --_\\\  '"${esc}${colors2}\n"
printf "\n"
